/**
 * Security Headers
 * 
 * Centralized security headers for all HTTP responses.
 * Used by Edge Functions and API layer.
 */

export const SECURITY_HEADERS: Record<string, string> = {
  'X-Content-Type-Options': 'nosniff',
  'X-Frame-Options': 'DENY',
  'Referrer-Policy': 'no-referrer',
  'Strict-Transport-Security': 'max-age=31536000; includeSubDomains',
  'Permissions-Policy': 'camera=(), microphone=(), geolocation=(), interest-cohort=()',
  'Content-Security-Policy': [
    "default-src 'self'",
    "script-src 'self'",
    "style-src 'self' 'unsafe-inline' https://fonts.googleapis.com",
    "font-src 'self' https://fonts.gstatic.com",
    "img-src 'self' data:",
    "connect-src 'self' https://*.supabase.co",
    "frame-ancestors 'none'",
    "base-uri 'self'",
    "form-action 'self'",
  ].join('; '),
};

export const CORS_HEADERS: Record<string, string> = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers':
    'authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version',
};

/** Merge all response headers (CORS + Security + Content-Type) */
export function getResponseHeaders(contentType = 'application/json'): Record<string, string> {
  return {
    ...CORS_HEADERS,
    ...SECURITY_HEADERS,
    'Content-Type': contentType,
  };
}

/** CORS-only headers for OPTIONS preflight */
export function getCorsHeaders(): Record<string, string> {
  return { ...CORS_HEADERS };
}
